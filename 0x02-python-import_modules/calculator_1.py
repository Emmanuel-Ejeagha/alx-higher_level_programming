#!/usr/bin/python3
def add(a, b):
    """My addiction function

    Args:
        a: first integer
        b: second integer

    Returns:
        The return value. a + b
    """
    return (a + b)

def sub(a, b):
    """my subtraction function

    Args:
        a: first value
        b: second value
	
    Returns:
		The return value. a - b
    """
    return (a - b)

def mul(a, b):
    """My multiplication function

    Args:
        a: first value
        b: second value

    Returns:
        The return value. a * b
    """
    return (a * b)

def div(a, b):
    """This is my division function

    Args:
        a: first integer
        b: second integer

    Returns:
        The return value. a / b
	"""
    return (a / b)
